package com.example.demo;

public class Potato {

	private int Id;
	
	private String color;
	private String shape;
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getShape() {
		return shape;
	}
	public void setShape(String shape) {
		this.shape = shape;
	}
	
}
